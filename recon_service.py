def filter_outstanding_for_tpa(out_clean: pd.DataFrame, tpa_choice: str) -> pd.DataFrame:
    if "Bill Company Name" not in out_clean.columns:
        raise ValueError("Outstanding cleaned is missing 'Bill Company Name' - cannot filter by TPA.")

    if tpa_choice == "IHX (Original MIS)":
        return out_clean.copy()

    if tpa_choice not in _OUT_FILTER_RULES:
        raise ValueError(
            f"Non-IHX TPA detected ('{tpa_choice}') but no TPA-wise outstanding filter rule is defined yet. "
            f"Add it to _OUT_FILTER_RULES before running."
        )

    tokens = [t.upper() for t in _OUT_FILTER_RULES[tpa_choice] if str(t).strip()]
    bn = out_clean["Bill Company Name"].astype(str).str.upper()

    mask = False
    for tok in tokens:
        mask = mask | bn.str.contains(tok, na=False)

    out_filt = out_clean.loc[mask].copy()

    if out_filt.empty:
        raise ValueError(
            f"TPA-wise outstanding filter produced 0 rows for TPA '{tpa_choice}'. "
            f"This is fail-fast to prevent cross-TPA matching chaos. "
            f"Check Bill Company Name values and update _OUT_FILTER_RULES tokens."
        )
    return out_filt.reset_index(drop=True)

# ==========================================
#  NON-IHX MATCH PLAN REGISTRY (TPA-GATED)
# ==========================================

def run_step4_plan(step3_df: pd.DataFrame, out_input, tpa_choice: str, keep_internal: bool = False) -> Tuple[pd.DataFrame, pd.DataFrame]:
    if tpa_choice == "IHX (Original MIS)":
        return step4_strict_matches(step3_df, out_input, deduplicate=True, keep_internal=keep_internal)

    if tpa_choice in [
        "PARK MEDICLAIM INSURANCE TPA PRIVATE LIMITED",
        "HEALTH INDIA INSURANCE TPA SERVICES PRIVATE LTD.",
        "FUTURE GENERALI",
        "RELIANCE",
        "ERICSON",
        "ADITYA BIRLA",
        "SAFEWAY INSURANCE TPA PVT.LTD",
        "STAR HEALTH & ALLIED HEALTH INSURANCE CO.LTD.",
        "HERITAGE HEALTH INSURANCE TPA PRIVATE LIMITED",
        "MEDSAVE HEALTHCARE TPA PVT LTD",
    ]:
        return step4_alpha_strict_unique(step3_df, out_input, deduplicate=True, match_mode="ALPHA_STRICT_UNIQUE", keep_internal=keep_internal)

    if tpa_choice == "VIDAL":
        m1, rem = step4_split_alpha_unique(step3_df, out_input, deduplicate=True, match_mode="VIDAL_SPLIT_ALPHA_UNIQUE", keep_internal=keep_internal)
        if m1.empty:
            return step4_alpha_strict_unique(step3_df, out_input, deduplicate=True, match_mode="VIDAL_ALPHA_UNIQUE", keep_internal=keep_internal)
        return m1, rem

    if tpa_choice in [
        "GOOD HEALTH",
        "VOLO HEALTH INSURANCE TPA PVT.LTD (EWA) (Mail Extract)",
        "SBI GENERAL",
    ]:
        return step4_numeric_split_unique(step3_df, out_input, deduplicate=True, match_mode="NUMERIC_SPLIT_UNIQUE", min_len=6, keep_internal=keep_internal)

    if tpa_choice == "CARE HEALTH INSURANCE LIMITED":
        m1, rem = step4_alpha_strict_unique(step3_df, out_input, deduplicate=True, match_mode="ALPHA_STRICT_UNIQUE", keep_internal=keep_internal)
        m2, rem2 = step4_care_numeric_fallback(rem, out_input, deduplicate=True, keep_internal=keep_internal)
        if not m2.empty:
            m1 = pd.concat([m1, m2], ignore_index=True)
        return m1, rem2

    if tpa_choice == "FHPL":
        m1, rem = step4_alpha_strict_unique(step3_df, out_input, deduplicate=True, match_mode="ALPHA_STRICT_UNIQUE", keep_internal=keep_internal)
        m2, rem2 = step4_fhpl_numeric_fallback(rem, out_input, deduplicate=True, keep_internal=keep_internal)
        if not m2.empty:
            m1 = pd.concat([m1, m2], ignore_index=True)
        return m1, rem2

    if tpa_choice == "ICICI LOMBARD":
        m1, rem = step4_alpha_strict_unique(step3_df, out_input, deduplicate=True, match_mode="ALPHA_STRICT_UNIQUE", keep_internal=keep_internal)
        m2, rem2 = step4_icici_lombard_split_fallback(rem, out_input, deduplicate=True, keep_internal=keep_internal)
        if not m2.empty:
            m1 = pd.concat([m1, m2], ignore_index=True)
        return m1, rem2

    raise ValueError(
        f"Non-IHX TPA detected ('{tpa_choice}') but no Step-4 match plan is defined in run_step4_plan()."
    )

