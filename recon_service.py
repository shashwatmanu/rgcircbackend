def _assert_exists(p: Path, label: str):
    if not p.exists():
        raise FileNotFoundError(f"{label} not found in runtime: {p}")

def _run_recon_core(bank_path: Path, mis_path: Path, out_path: Path, run_dir: Path = None) -> Dict[str, object]:
    run_id = f"{bank_path.stem}__{mis_path.stem}"

    bank_path = normalize_to_xlsx(bank_path)
    mis_path  = normalize_to_xlsx(mis_path)
    out_path  = normalize_to_xlsx(out_path)

    BANK_TYPE = detect_bank_type(bank_path)
    TPA_CHOICE = detect_tpa_choice(mis_path)

    if BANK_TYPE == "ICICI":
        bank_df = clean_raw_bank_statement_icici(bank_path)
    else:
        bank_df = clean_raw_bank_statement_axis(bank_path)

    mis_clean = parse_mis_universal(mis_path, tpa_name=TPA_CHOICE, empty_threshold=0.5)
    step3_candidate_df, step3_unmatched_bank = step2_match_bank_mis_by_utr(
        bank_df, mis_clean, TPA_CHOICE, deduplicate=True, min_key_len=8
    )

    out_clean = parse_outstanding_excel_to_clean(out_path)

    step3_auto, step3_review, step3_full, step3_metrics = classify_step3_matches(
        step3_candidate_df, BANK_TYPE, TPA_CHOICE, run_id
    )

    if TPA_CHOICE == "IHX (Original MIS)":
        step4_candidate_df, unmatched_after_step4 = step4_strict_matches(
            step3_auto, out_path, deduplicate=True, keep_internal=True
        )
        out_filt = out_clean.copy()
    else:
        out_filt = filter_outstanding_for_tpa(out_clean, TPA_CHOICE)
        out_for_step4_path = run_dir / "06_outstanding_filtered.xlsx" if run_dir is not None else Path("06_outstanding_filtered.xlsx")
        out_filt.to_excel(out_for_step4_path, index=False)

        step4_candidate_df, unmatched_after_step4 = run_step4_plan(
            step3_auto, out_for_step4_path, TPA_CHOICE, keep_internal=True
        )

    step4_auto, step4_review, step4_full, step4_metrics = classify_step4_matches(
        step4_candidate_df, BANK_TYPE, TPA_CHOICE, run_id
    )

    final_review = (
        pd.concat([step3_review, step4_review], ignore_index=True)
        if (not step3_review.empty or not step4_review.empty)
        else pd.DataFrame()
    )

    metrics = {
        "RUN_ID": run_id,
        "BANK_TYPE": BANK_TYPE,
        "TPA_CHOICE": TPA_CHOICE,
        "bank_rows_clean": len(bank_df),
        "mis_rows_clean": len(mis_clean),
        "outstanding_rows_clean": len(out_clean),
        "outstanding_rows_filtered": len(out_filt),
        "step3_candidate_pairs": step3_metrics["step3_candidate_pairs"],
        "step3_auto_rows": step3_metrics["step3_auto_rows"],
        "step3_review_rows": step3_metrics["step3_review_rows"],
        "step3_unmatched_bank_rows": len(step3_unmatched_bank),
        "step4_candidate_pairs": step4_metrics["step4_candidate_pairs"],
        "step4_auto_rows": step4_metrics["step4_auto_rows"],
        "step4_review_rows": step4_metrics["step4_review_rows"],
        "step4_unmatched_rows": len(unmatched_after_step4),
        "final_auto_rows": len(step4_auto),
        "final_review_rows": len(final_review),
    }

    outputs = {
        "BANK_TYPE": BANK_TYPE,
        "TPA_CHOICE": TPA_CHOICE,
        "bank_df": bank_df,
        "mis_clean": mis_clean,
        "step3_full": step3_full,
        "step3_auto": step3_auto,
        "step3_review": step3_review,
        "step3_unmatched_bank": step3_unmatched_bank,
        "out_clean": out_clean,
        "out_filt": out_filt,
        "step4_full": step4_full,
        "step4_auto": step4_auto,
        "step4_review": step4_review,
        "final_review": final_review,
        "metrics": metrics,
    }
    return outputs

def _save_run_outputs(outputs: Dict[str, object], run_dir: Path):
    run_dir.mkdir(exist_ok=True, parents=True)

    outputs["bank_df"].to_excel(run_dir / "01_bank_clean.xlsx", index=False)
    outputs["mis_clean"].to_excel(run_dir / "04_mis_cleaned.xlsx", index=False)
    outputs["step3_full"].to_excel(run_dir / "05_bank_mis_mapped.xlsx", index=False)
    outputs["step3_review"].to_excel(run_dir / "05_step3_review.xlsx", index=False)
    outputs["out_clean"].to_excel(run_dir / "06_outstanding_cleaned.xlsx", index=False)

    if outputs["TPA_CHOICE"] != "IHX (Original MIS)":
        outputs["out_filt"].to_excel(run_dir / "06_outstanding_filtered.xlsx", index=False)

    outputs["step4_review"].to_excel(run_dir / "06_step4_review.xlsx", index=False)
    outputs["step4_auto"].to_excel(run_dir / "07_final_posting_sheet.xlsx", index=False)
    outputs["final_review"].to_excel(run_dir / "07_review_queue.xlsx", index=False)
    build_metrics_df(outputs["metrics"]).to_excel(run_dir / "07_run_metrics.xlsx", index=False)
    build_documentation_workbook(run_dir / "00_run_documentation.xlsx")

def run_single_lane():
    print("\nEnter Filenames (files must already be uploaded in Colab runtime sidebar):")
    bank_file = input("Bank File (e.g. bank.xlsx): ").strip()
    mis_file  = input("MIS File  (e.g. mis.xlsm): ").strip()
    out_file  = input("Outstanding File (e.g. outstanding.xlsx): ").strip()

    if not bank_file or not mis_file or not out_file:
        raise ValueError("Bank/MIS/Outstanding filenames are required.")

    bank_path = Path(bank_file)
    mis_path  = Path(mis_file)
    out_path  = Path(out_file)

    _assert_exists(bank_path, "Bank file")
    _assert_exists(mis_path, "MIS file")
    _assert_exists(out_path, "Outstanding file")

    outputs = _run_recon_core(bank_path, mis_path, out_path, run_dir=Path("."))

    _save_run_outputs(outputs, Path("."))

    print(f"\nBank: {outputs['BANK_TYPE']} | TPA: {outputs['TPA_CHOICE']}")
    for k, v in outputs["metrics"].items():
        if k in ("RUN_ID", "BANK_TYPE", "TPA_CHOICE"):
            continue
        print(f"{k}: {v}")

def run_bulk_mode():
    print("\nBULK MODE — files already uploaded in Colab runtime sidebar")
    bank_input = input("Enter BANK filenames (comma separated): ").strip()
    mis_input  = input("Enter MIS filenames (comma separated): ").strip()
    out_file   = input("Outstanding filename (single file): ").strip()

    if not bank_input or not mis_input or not out_file:
        raise ValueError("Bank files, MIS files, and Outstanding file are all required.")

    bank_files = [Path(x.strip()) for x in bank_input.split(",") if x.strip()]
    mis_files  = [Path(x.strip()) for x in mis_input.split(",") if x.strip()]
    out_path   = Path(out_file.strip())

    for p in bank_files:
        _assert_exists(p, "Bank file")
    for p in mis_files:
        _assert_exists(p, "MIS file")
    _assert_exists(out_path, "Outstanding file")

    output_root = Path("bulk_outputs")
    output_root.mkdir(exist_ok=True)

    summary_rows = []

    print(f"\nFound {len(bank_files)} bank file(s) and {len(mis_files)} MIS file(s).")
    print(f"Outstanding: {out_path.resolve()}")
    print(f"Output root: {output_root.resolve()}")

    for bank_path in bank_files:
        for mis_path in mis_files:
            run_id = f"{bank_path.stem}__{mis_path.stem}"
            print(f"\n--- RUN: {run_id} ---")

            try:
                temp_run_dir = output_root / "temp"
                temp_run_dir.mkdir(parents=True, exist_ok=True)
                outputs = _run_recon_core(bank_path, mis_path, out_path, run_dir=temp_run_dir)

                run_dir = output_root / f"{bank_path.stem}__{outputs['TPA_CHOICE']}__{outputs['BANK_TYPE']}"
                _save_run_outputs(outputs, run_dir)

                print(
                    f"OK | Final AUTO rows: {len(outputs['step4_auto'])} "
                    f"| Review rows: {len(outputs['final_review'])} "
                    f"| Folder: {run_dir}"
                )

                row = {
                    "bank_file": bank_path.name,
                    "mis_file": mis_path.name,
                    "bank_type": outputs["BANK_TYPE"],
                    "tpa": outputs["TPA_CHOICE"],
                    "status": "OK",
                    "error": "",
                }
                row.update(outputs["metrics"])
                summary_rows.append(row)

            except Exception as e:
                print(f"FAILED | {run_id} | {e}")
                summary_rows.append({
                    "bank_file": bank_path.name,
                    "mis_file": mis_path.name,
                    "bank_type": "",
                    "tpa": "",
                    "status": "FAILED",
                    "error": str(e)
                })

    summary_df = pd.DataFrame(summary_rows)
    summary_df.to_excel("bulk_summary.xlsx", index=False)
    print("\nSaved bulk_summary.xlsx")
    print("Done.")

